func TestNewEventChannelID(t *testing.T) {
	id := NewEventChannelID("12345678-1234-9876-4563-123456789012", "example-resource-group", "partnerNamespaceValue", "eventChannelValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.PartnerNamespaceName != "partnerNamespaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'PartnerNamespaceName'", id.PartnerNamespaceName, "partnerNamespaceValue")
	}

	if id.EventChannelName != "eventChannelValue" {
		t.Fatalf("Expected %q but got %q for Segment 'EventChannelName'", id.EventChannelName, "eventChannelValue")
	}
}

