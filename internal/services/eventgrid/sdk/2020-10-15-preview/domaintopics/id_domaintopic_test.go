var _ resourceids.ResourceId = DomainTopicId{}

func TestNewDomainTopicID(t *testing.T) {
	id := NewDomainTopicID("12345678-1234-9876-4563-123456789012", "example-resource-group", "domainValue", "domainTopicValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DomainName != "domainValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DomainName'", id.DomainName, "domainValue")
	}

	if id.DomainTopicName != "domainTopicValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DomainTopicName'", id.DomainTopicName, "domainTopicValue")
	}
}

func TestFormatDomainTopicID(t *testing.T) {
	actual := NewDomainTopicID("12345678-1234-9876-4563-123456789012", "example-resource-group", "domainValue", "domainTopicValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.EventGrid/domains/domainValue/topics/domainTopicValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", actual, expected)
	}
}

